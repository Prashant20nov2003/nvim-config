			callback = function()
				local stats = require("lazy").stats()
				local count = (math.floor(stats.startuptime * 100) / 100)
				dashboard.section.footer.val = {
					"󱐌 " .. stats.count .. " plugins loaded in " .. count .. " ms",
					" ",
